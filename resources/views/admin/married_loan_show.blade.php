    function init_multiple(id,initialPreview,initialPreviewConfig,order_id)
    {
        $('#'+id).fileinput('destroy');
        $('#'+id).fileinput({
            language: 'zh',
            uploadUrl: api+'/api/married_upload?name='+id+'&order_id='+order_id,
            allowedFileExtensions : ['jpg', 'png','gif','jpeg'],
            overwriteInitial: false,
            maxFileSize: 2000,
            maxFilesNum: 10,
            dropZoneEnabled: false,
            uploadAsync: true,
            showUpload:true,
            initialPreview: initialPreview,
            initialPreviewConfig: initialPreviewConfig,
            showType:'detail',
            layoutTemplates :{
                actionUpload:'',//去除上传预览缩略图中的上传图片
            },

        });
    }
    // //ajax 获得资源初始化多图
    function get_images(id,url,order_id)
    {
        $.ajax({
            type:'GET',
            url:url+order_id+'&name='+id,
            success:function(res){
                console.log(res);
                if(res){
                    init_multiple(id,res.images,res.delete,order_id);
                }else{
                    init_multiple(id,[],[],order_id);
                }

            },
            error: function (res) {
                console.log(res);
            }
        })
    }
    function getUrlParam(str){
        var reg = new RegExp("(^|&)" + str + "=([^&]*)(&|$)"); //构造一个含有目标参数的正则表达式对象
        var r = window.location.search.substr(1).match(reg);  //匹配目标参数
        if (r != null) return unescape(r[2]); return null; //返回参数值
    }
